#include<iostream>
#include<cmath>
using namespace std;
int main(){
    cout<<"Square Root Of 4 is "<<sqrt(4)<<endl;
    cout<<"Cube Root Of 81 is "<<cbrt(81)<<endl;
    cout<<"Minimum is "<<min(4,81)<<endl;
    cout<<"Maximum is "<<max(4,81)<<endl;
    cout<<"4 Raised to Power 8 is "<<pow(8,4)<<endl;  
}